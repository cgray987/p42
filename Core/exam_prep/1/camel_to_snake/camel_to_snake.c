#include <unistd.h>

int	main(int ac, char **av)
{
	char *str;
	int	i = 0;

	if (ac == 2)
	{
		str = av[1];
		while (str[i])
		{
			if (str[i] >= 'A' && str[i] <= 'Z')
			{
				str[i] += 32;
				write(1, "_", 1);
			}
			write(1, &str[i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
