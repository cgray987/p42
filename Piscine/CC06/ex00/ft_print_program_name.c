#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	int	i;

	(void)argc;
	i = 0;
	while (argv[0][i] != '\0')
	{
		putchar(argv[0][i]);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
